<h2>Try a "Live CD"</h2>

<p>You can try Linux for real without even needing to touch your 
hard disk, without needing to do anything to your existing Windows !</p>

<p>This is possible with "Linux live CDs". A live CD works the 
following way :</p>

<ul>

<li>Download a CD image from the web (this will be an <tt>.iso</tt> 
file, about 650 MB). </li>

<li>Burn it onto a blank CD.</li>

<li>Put the CD in your CD drive, and reboot.</li>

<li>Your computer will boot on the CD instead of booting on the 
hard drive (where Windows is), and will <b>not touch anything</b> : 
don't worry about Windows and your data, they'll still be there when you 
reboot again without the CD. </li>

<li>If your computer still boots Windows (the CD doesn't seem to 
have any effect), then your BIOS configuration is faulty: you need to 
ask it to first scan the CD drive for an operating system, and then the 
hard drive. You can usually access the BIOS by typing "Del" or "F2" 
while booting (if you don't feel comfortable with this, get some 
help).</li>

<li>And voil√†, Linux runs on your system, you can test it and see 
how you like it! Don't worry if the system seems a bit slow right 
now: it takes some time to load everything from the CD. Your Linux 
system will be much quicker once actually installed on your hard disk. 
</li>

</ul>

<p>So, where can you download those CD images ? There are a lot of 
Linux Live CDs available on the web ! You could try one of these for 
example:</p>

