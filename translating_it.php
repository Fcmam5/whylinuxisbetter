<h2>Come tradurre questo sito</h2>

<ol>

<li>Inviami un'<b>email</b> a ideas [at] whylinuxisbetter [punto] net per assicurarti che non ci sia nessuno già al lavoro per una certa traduzione.</li>

<li>Scegli un <b>suffisso</b> unico e breve per la tua lingua (ad esempio, "ru" 
per il Russo, "ser" per il Serbo). Chiameremo questo suffisso "lang" per il resto del tutorial.</li>

<li><b>Scarica</b> ed estrai <a 
href="http://www.manucornet.net/pub/www.whylinuxisbetter.net.tgz">questo archivio</a>.</li>

<li>Cerca di usare un editor di testo semplice (ad esempio gedit in Linux) e assicurati di aprire e salvare i file con la codifica 
<b>UTF-8</b>. Con questo editor, crea un file chiamato title_lang.txt nella root dell'archivio estratto e scrivi all'interno la traduzione del titolo di questo sito.</li>

<li>Vai nella sottocartella "items". Qui troverai una cartella per ogni articolo del sito. In ognuna di esse,

<ul>

<li>Copia il file index.php in index_lang.php&nbsp;.</li>

<li>Apri questo file appena creato, trova le parti in cui c'è del testo in inglese e sostituiscile con la tua traduzione (non c'è bisogno che capisci l'HTML o il PHP, ti basta lasciare le parti che non capisci così come sono). </li>

</ul>
</li>

<li>Fai la stessa cosa per i file nelle cartelle "stick_to_windows" e "switch".</li>

<li>Nella root dell'archivio, copia il file principale index.php in 
index_lang.php. Questo contiene una riga per ognuno dei vari "items": 
per ogni riga, sostituisci la stringa vuota "" con un underscore e il suffisso della tua lingua: "_lang" e traduci il titolo di questo item.</li>

<li><b>Elimina tutti i file che non hai toccato, includi solo le nuove traduzioni</b> e possibilmente i file in cui hai corretto errori.</li>

<li><b>Comprimi</b> la cartella risultante in TAR o in <b>Gzipped tar</b> (estensioni
.tar, .tgz o .tar.gz). Puoi anche zipparla, ma sarà meno conveniente per me.</li>

<li>Inviami il file per <b>email</b> (stesso indirizzo di prima). In questa email, ricordati di includere il nome con il quale desideri apparire e la tua pagina web alla quale posso linkarlo, se ne hai una.</li>

<li>Guarda la traduzione apparire nel sito con il tuo nome nella prima pagina della tua lingua e ricevi un miliardo di ringraziamenti da me e soprattutto da tutte le persone che parlano la tua stessa lingua!!</li>

</ol>

Grazie mille!!

